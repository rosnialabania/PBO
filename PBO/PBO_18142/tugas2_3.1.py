a=250
b=50
#penjumlahan
tambah=a + b
print("hasil penjumlahan:", tambah)

#pengurangan
kurang=a - b
print("hasil kurang:", kurang)

#pembagian
bagi=a / b
print("hasil bagi:", bagi)
#perkalian
kali=a * b
print("hasil kali:", kali)

#pangkat
pangkat_a=a**2
print("hasil a^2=", pangkat_a)

#sisa bagi
sisa_bagi=a % b
print("sisa bagi:", sisa_bagi)
